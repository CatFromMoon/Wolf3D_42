#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char	*b;
	int		i;

	i = 0;
	b = dst;
	while (src[i] != '\0')
	{
		b[i] = src[i];
		i++;
	}
	b[i] = '\0';
	return (b);
}
